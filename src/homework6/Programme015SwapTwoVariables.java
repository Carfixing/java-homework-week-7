package homework6;

public class Programme015SwapTwoVariables {
    public static void main(String[]args) {
        int a = 10;
        int b = 20;
        int c;

        c = a;
        a = b;
        b = c;

        System.out.print("a=");
        System.out.println(a);
        System.out.print("b=");
        System.out.println(b);
    }
}
